(2) 火災、停電、天災などの不可抗力により、本サービスの提供が困難な場合。

(3) 第一種電気通信事業者の任務が提供されない場合。

(4) その他、運用上あるいは技術上当社が本サービスの一時中断、もしくは停止が
　　必要であるか、又は不測の事態により、当社が本サービスの提供が困難と
　　判断した場合。



2 当社は、本サービスの提供の一時中断、停止等の発生により、利用者または
　第三者が被ったいかなる不利益、損害について、理由を問わず一切の責任を
　負わないものとします。


