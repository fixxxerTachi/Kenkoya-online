第３条（利用者の地位及び制限事項）


1 利用者の地位

　健康屋　において利用者は、提供される本サービスのいずれかを享受する
　時点において（ここにいう享受には、情報の閲覧も含みます）、本規約に合意
　したものとみなされ、同時に健康屋　における利用者としての地位を
　得るものとします。

2 利用者に対する制限事項

　利用者は、以下に掲げる行為は行ってはならないものとします。

(1) 健康屋　が指定した方法以外の方法によって、健康屋　を利用する行為。

(2) 他者になりすまして本サービスを利用する行為。

(3) 健康屋　が認める以外の方法で、本サービスに関連するデータのリンクを、
　　他のデータ等へ指定する行為。

(4) 健康屋　を利用するコンピュータに保存されているデータへ不正アクセスする、
　　又はこれを破壊もしくは破壊するおそれのある行為。

(5) 本サービスの運営を妨害する行為。

(6) 本サービスを使用した営業活動並びに営利を目的とした利用及びその準備を目的
　　とした利用。但し、当社が別途承認した場合には、この限りではありません。

(7) 他の利用者の個人情報を収集したり、蓄積すること、又はこれらの行為を
　　しようとする事。

(8) 公序良俗に反する行為及びその他国内外の法令に反する行為。



